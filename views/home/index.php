<div id="home-content">
    <p>Welcome to <strong>Tuneopedia</strong> &mdash; a collaborative encyclopedia for traditional music.</p>

    <p>Browse and search thousands of tunes from the Irish, Scottish, and old-time traditions.
       Each tune includes ABC notation that you can view, listen to, and contribute your own settings for.</p>

    <h3>What you can do</h3>
    <ul>
        <li><strong>Browse Tunes</strong> &mdash; Explore tunes organized by type: reels, jigs, hornpipes, and more.</li>
        <li><strong>View Notation</strong> &mdash; Click the notation icon next to any tune to see its sheet music rendered instantly.</li>
        <li><strong>Live Playback</strong> &mdash; The ABC editor renders notation in real-time as you type, with built-in keystroke playback so you can hear your music as you type.</li>
        <li><strong>Add Favorites</strong> &mdash; Save tunes to your personal favorites list for quick access.</li>
        <li><strong>Build Collections</strong> &mdash; Group your favorite tunes into custom collections for sessions, gigs, or study.</li>
        <li><strong>Import Collections</strong> &mdash; Upload an ABC file or paste ABC notation directly to import an entire collection of tunes at once.</li>
        <li><strong>Tablature</strong> &mdash; View tablature for fiddle, mandolin, guitar, and 5-string banjo with support for multiple tunings including Open G, Double C, Sawmill, and more.</li>
        <li><strong>Contribute</strong> &mdash; Add new tunes, submit alternate settings, and vote on the best transcriptions.</li>
        <li><strong>Discuss</strong> &mdash; Join the discussion forum to talk about jams, sessions, festivals, and traditional music.</li>
    </ul>

    <p>Create an account to start building your favorites and contributing to the community.</p>
